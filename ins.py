# assigning integer literals to variables
a = 10
b = -5
c = 0

print("integer values:")
print("a =",a)
print("b =",b)
print("c =",c)

#performing operations on integer literals
print("sum of a and b:",a+b)
print("product of and c:",a*c)
print("difference between a and b:",a-b)

#assigning floating point literals to variables
a = 3.14
b = -2.718
c = 0.0

#performing operation with floating-point numbers
sum_float = a+b
product_float = a*b
differente_float = a-b

#printing floating-point values and rules of operations
print("\nfloating-point values:")
print("a =",a)
print("b =",b)
print("c =",c)
print("sum of a and b:", sum_float)
print("product of a and b:",product_float)
print("difference between a and b:",differente_float)


